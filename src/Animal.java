public class Animal {
    private String yanzhi;
    private int mingjiao;

    public Animal(String yanzhi, int mingjiao) {
        this.yanzhi = yanzhi;
        this.mingjiao = mingjiao;
    }

    public String getYanzhi() {
        return yanzhi;
    }

    public void setYanzhi(String yanzhi) {
        this.yanzhi = yanzhi;
    }

    public int getMingjiao() {
        return mingjiao;
    }

    public void setMingjiao(int mingjiao) {
        this.mingjiao = mingjiao;
    }
}
